from python99.graph.p603 import cycle

def test_cycle():
    assert cycle((['a', 'b', 'c', 'd', 'e', 'f', 'g', 'h'], 
                    [
                        ('a', 'b'), 
                        ('a', 'd'), 
                        ('b', 'c'), 
                        ('b', 'e'),
                        ('c', 'e'), 
                        ('d', 'e'), 
                        ('d', 'f'), 
                        ('d', 'g'), 
                        ('e', 'h'), 
                        ('f', 'g'), 
                        ('g', 'h')
                    ]), 'a') == [
                                    ['a', 'b', 'a'],
                                    ['a', 'b', 'c', 'e', 'd', 'a'],
                                    ['a', 'b', 'c', 'e', 'h', 'g', 'd', 'a'],
                                    ['a', 'b', 'c', 'e', 'h', 'g', 'f', 'd', 'a'],
                                    ['a', 'b', 'e', 'd', 'a'],
                                    ['a', 'b', 'e', 'h', 'g', 'd', 'a'],
                                    ['a', 'b', 'e', 'h', 'g', 'f', 'd', 'a'],
                                    ['a', 'd', 'a'],
                                    ['a', 'd', 'e', 'b', 'a'],
                                    ['a', 'd', 'e', 'c', 'b', 'a'],
                                    ['a', 'd', 'f', 'g', 'h', 'e', 'b', 'a'],
                                    ['a', 'd', 'f', 'g', 'h', 'e', 'c', 'b', 'a'],
                                    ['a', 'd', 'g', 'h', 'e', 'b', 'a'],
                                    ['a', 'd', 'g', 'h', 'e', 'c', 'b', 'a']
                                ]